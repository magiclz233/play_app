/** 原始色板类型 */
export type PrimitiveScale = typeof primitives;

/** 色相名称 */
export type HueName = keyof Omit<PrimitiveScale, 'white' | 'black'>;

/** 色阶级别 */
export type ScaleLevel = 50 | 100 | 200 | 300 | 400 | 500 | 600 | 700 | 800 | 900 | 950;

/**
 * 快捷读取色板值
 * @example getPrimitive('coral', 500) → '#FF3B5C'
 */
export function getPrimitive(hue: HueName, level: ScaleLevel): string {
  return primitives[hue][level];
}
